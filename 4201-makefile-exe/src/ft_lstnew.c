#include "../inc/libftprintf.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lstnew;

	lstnew = (t_list *)malloc(sizeof(t_list));
	if (!lstnew)
		return (NULL);
	else
	{
		(*lstnew).content = content;
		(*lstnew).next = NULL;
	}
	return (lstnew);
}
